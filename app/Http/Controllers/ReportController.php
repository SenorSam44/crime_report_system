            'reportingPerson' => Auth::id(),
            'crime' => $request->crime,
            'crimeTime' => $request->crimeTime,
            'reportTime' => $request->reportTime,
            'district' => $request->district,
            'thana' => $request->thana,
            'address' => $request->address,
        ]);

        return Redirect::back()->with('session', 'Report create Successfully');
